import SwiftUI

extension Color {
    static let background_light = Color(hex: "#F8FAFB")
    static let background_dark = Color(hex: "#293241")
    static let font_light = Color(hex: "#FFFFFF")
    static let font_dark = Color(hex: "#000000")

}

extension Color {
  init(hex: String) {
    let scanner = Scanner(string: hex)
    _ = scanner.scanString("#")
    
    var rgb: UInt64 = 0
    scanner.scanHexInt64(&rgb)
    
    let r = Double((rgb >> 16) & 0xFF) / 255.0
    let g = Double((rgb >>  8) & 0xFF) / 255.0
    let b = Double((rgb >>  0) & 0xFF) / 255.0
    self.init(red: r, green: g, blue: b)
  }
}
